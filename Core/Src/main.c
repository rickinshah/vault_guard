/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{

  /* USER CODE BEGIN 1 */
    uint8_t buff[255];
    //bool check = 0;
	uint8_t uid[MIFARE_UID_MAX_LENGTH];
	int32_t uid_len = 0;
	rfid_record_t records[NUM_RECORDS] = {
        (rfid_record_t) { { 0xB9, 0x7A, 0xC4, 0x59 }, 4, "Blue Tag"},
        (rfid_record_t) { { 0xC9, 0xB5, 0xA6, 0x5A }, 4, "Card!" }
    };
    
    size_t uid_str_size = 5 * MIFARE_UID_MAX_LENGTH + 1;
    char auth_uid_str[uid_str_size];
    bool authorized = false;
    uint8_t scan_loop = 0;
    bool new_line_needed = 0;
    uint32_t last_anim_time = 0;
    
    // Timer
    rfid_record_t last_scanned;
    uint32_t start_time = 0;
    uint32_t duration = 1 * 60 * 1000; // 1 minutes
    
  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_I2C1_Init();
  MX_USART2_UART_Init();
  /* USER CODE BEGIN 2 */
    PN532 pn532;
	PN532_Init(&pn532);
    
	if (PN532_GetFirmwareVersion(&pn532, buff) == PN532_STATUS_OK) {
        print("Handshake Done!\r\n");
	} else {
        print("Handshake Failed!\r\n");
		return -1;
	}
    
	PN532_SamConfiguration(&pn532);
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
      if(authorized && HAL_GetTick() - start_time >= duration) {
          authorized = false;
      }

    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
      uid_len = PN532_ReadPassiveTarget(&pn532, uid, PN532_MIFARE_ISO14443A, 1000);
      if (uid_len == PN532_STATUS_ERROR) {
          if(HAL_GetTick() - last_anim_time > 500) {
              last_anim_time = HAL_GetTick();
              if(new_line_needed) {
                  print("\r\n");
                  new_line_needed = false;
              }
              print("\rScan Card   ");
              print("\rScan Card");
              for(size_t i = 0; i < scan_loop; i++) {
                  print(".");
              }
              scan_loop = (scan_loop + 1) % 4;
          }
      }
      else {
          size_t uid_index = check_uid(records, NUM_RECORDS, uid, uid_len);
          if(uid_index == UID_NOT_FOUND) {
              authorized = false;
              uid_to_string(uid, uid_len, auth_uid_str, uid_str_size);
              print("\r\n[Access Denied] ");
              print(auth_uid_str);
          } else {
              last_scanned = records[uid_index];
              authorized = true;
              start_time = HAL_GetTick();
              uid_to_string(last_scanned.uid, last_scanned.uid_len, auth_uid_str, uid_str_size);
              print("\r\n[Access Granted] ");
              print(auth_uid_str);
              print(" - ");
              print(last_scanned.name);
              
          }
          new_line_needed = true;
      }

      // Check LDR
      if(!HAL_GPIO_ReadPin(GPIOA, GPIO_PIN_7)) {
          print("\r\n[Vault Opened] ");
          if(authorized) {
              print(auth_uid_str);
          } else {
            print("Intrusion");
          }
          new_line_needed = true;
      }
      // Check Vibration
      if(!HAL_GPIO_ReadPin(GPIOA, GPIO_PIN_6)) {
          print("\r\n[Vibration Detected] ");
          if(authorized) {
              print(auth_uid_str);
          }
          else {
              print("Intrusion");
          }
          new_line_needed = true;
      }
  }
  /* USER CODE END 3 */
}

